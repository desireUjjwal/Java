public class Demo11_TypesOfInterface {
    public static void main(String[] args) {
        // There are four types of interfaces in Java:
        //
        // 1. Normal Interface 
        //    --> Contains two or more abstract methods. 
        //        Example: interface A in Demo06_Interface.java
        //
        // 2. Marker Interface 
        //    --> Contains no methods or fields. 
        //        Used to mark a class for special behavior. 
        //        Example: Serializable, Cloneable
        //
        // 3. Functional Interface (Single Abstract Method Interface - SAM Interface) 
        //    --> Contains exactly one abstract method. 
        //        Commonly used with lambda expressions. 
        //        Example: Runnable, Comparator
        //
        // 4. Annotation Interface 
        //    --> Used to define custom or built-in annotations. 
        //        Declared using @interface keyword. 
        //        Example (custom): @MyAnnotation
        //        Example (built-in): @Override, @Deprecated

    }
}
