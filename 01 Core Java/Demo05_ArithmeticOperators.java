public class Demo5_ArithmaticOperators {
    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 4;
        System.out.println("Addition: " + (num1 + num2)); // 19
        System.out.println("Subtraction: " + (num1 - num2)); // 11
        System.out.println("Multiplication: " + (num1 * num2)); // 60
        System.out.println("Division: " + (num1 / num2)); // 3  
        System.out.println("Modulus: " + (num1 % num2)); // 3
        System.out.println("Increment: " + (++num1)); // 16
        System.out.println("Decrement: " + (--num2)); // 3
        System.out.println("Post-Increment: " + (num1++)); // 16
        System.out.println("Post-Decrement: " + (num2--)); // 3
    }
}
